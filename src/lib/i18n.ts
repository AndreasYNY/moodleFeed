const dictionaries = { en, id };

export type I18nKey = keyof typeof en;

export function translate(language: Language, key: I18nKey, params?: Record<string, Primitive>) {
  const template = dictionaries[language]?.[key] ?? en[key] ?? key;
  if (!params) return template;
  return Object.entries(params).reduce(
    (value, [param, replacement]) => value.split(`{${param}}`).join(String(replacement ?? '')),
    template,
  );
}

export function useI18n() {
  const language = useSettingsStore((state) => state.language);
  const t = useCallback(
    (key: I18nKey, params?: Record<string, Primitive>) => translate(language, key, params),
    [language],
  );
  return {
    language,
    dateLocale: language === 'id' ? idLocale : enUS,
    t,
  };
}
